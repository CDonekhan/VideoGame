package combat;

public class AttackType {
   // Attack Type Fields
   private int damage;
   private String name;

   
 // Setters and getters  
public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

public int getDamage() {
	return damage;
}

public void setDamage(int damage) {
	this.damage = damage;
}
   
}
